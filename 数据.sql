CREATE  TABLE student(

 sid     INT(11) PRIMARY KEY NOT NULL,

 sname   CHAR(25) NOT NULL,

 age     INT(11) NOT NULL,

 sex     CHAR(2) NOT NULL,

 department CHAR(40) ,

 address  CHAR(200) ,

 birthplace  VARCHAR(256)

);

CREATE  TABLE sc(

  sid   INT(11) NOT NULL,

  cid   INT(11) NOT NULL,

  grade INT(11)

);

CREATE  TABLE course(

  cid    INT(11) NOT NULL PRIMARY KEY DEFAULT 4,

  cname  CHAR(40),

  teacher  CHAR(40)

);

